def multiplicar(num1, num2):
    """
    Función que multiplica 2 números
    Argumentos:
    numero1 (int)
    numero2 (int)
    Retorna la multiplicación de los números dados.
    
    >>> multiplicar(2,3)
    6
    >>> multiplicar(2,4)
    8
    """
    return num1 * num2

print(f"El resultado de la multiplicación es {multiplicar(6,12)}")
