# Funciones útiles
# - Redondeo
print(round(3.45546))

# - Valor absoluto
print(abs(-23))

# - Crear una lista
lista = [2,65,1,23,12,7,4,9,53,121]
print(lista)

# - Mínimo de la lista
print(min(lista))

# - Máximo de la lista
print(max(lista))

# - Ordenar la lista
print(sorted(lista))

# - Suma total de la lista
print(sum(lista))